<div class="footer-elem page grey">
  <h2 class="center-text red-font ">La sélectivité</h2>

</br>

<p>
  Il y a deux manières d’être un professionnel référencé sur ProLinked
</p>

<p>
  - Une personne de votre entourage ou de vos relations vous a recommandé afin que vous puissiez faire partie des professionnels référencés sur - ProLinked. Vous allez donc recevoir un SMS ou Email avec le lien vous permettant de rejoindre le site. Un code d’accès vous sera également envoyé pour vous authentifier. Pour recommander un professionnel il suffit d’indiquer son Email ou N° Téléphone ainsi que son nom dans la rubrique « Recommander » de ProLinked. Un code d’accès lui sera alors envoyé qui lui permettra de s’authentifier et d’être aussitôt visible sur ProLinked.
</p>

<p>
  Ou bien
</p>

<p>
  - Rendez-vous dans la rubrique « Devenir Professionnel » et demandez une invitation. Vous devrez alors renseigner vos coordonnés en attendant de recevoir cette invitation pour pouvoir commencer à être visible sur ProLinked. Tout le monde n’aura pas la chance de recevoir cette invitation.
</p>

<p>
  Pour qu’un professionnel soit sur d’être référencé sur ProLinked, il est donc préférable d’être recommandé par une personne de votre entourage.
</p>

<p>
  Vous devez avoir un compte ProLinked pour recommander un professionnel ou pour demander une invitation. Pour toutes questions supplémentaire merci de nous contacter (contact@prolinked.fr).
</p>

