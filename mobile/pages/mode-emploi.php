  </p>
  <p>
    Il se compose de ses informations personnelles et professionnelles :

  </p>

  <ul>
    <li>Nom, Prénom</li>


<li>    Lieu de travail</li>
<li>Compétences
</li><li>    Expériences</li>
    <li>Recommandations</li>
<li>    Relations en commun
</li>   <li> Photo de profil</li>
<li>   Photos / Albums</li>
</li>    <li>Taux et délai de réponse</li>
<li>    Commentaires
</li>    <li>Intérêts</li>
<li>    Contacts et relations en commun
</li>
  </ul>

<p>    Les coordonnées du professionnel ne sont pas communiquées sur son profil de manière à maintenir l’échange et la communication entre clients et professionnels sur la plateforme ProLinked. Ainsi, les coordonnées du professionnel restent privées tant que le professionnel n’a pas de demandes de rendez-vous confirmées.
</p>
<p>    Vous pouvez, envoyer une demande de rendez-vous (ou message) au professionnel sur son profil, l’ajouter à vos contacts, ainsi que le recommander à la communauté ProLinked.
    Enfin, vous pourrez laisser un commentaire sur le profil du professionnel uniquement si vous avez bénéficiez de ses services.</p>

<p>    Tous les profils des professionnels sont vérifiés. N’apparaitront sur ProLinked uniquement des professionnels qualifié et de qualité.
</p>

