<div class="footer-elem page grey">

  <h2 class="center-text red-font ">Page d'accueil</h2>
</br>
  <p>La page d’accueil est la première page que l’utilisateur voit lorsqu’il entre sur le site.</p>
  <p>L’utilisateur pourra indiquer directement le service ou le professionnel qu’il souhaite dans la barre de recherche.</p>
  <p>Mais il pourra également rechercher un professionnel ou un service en parcourant les rubriques classées par type de professions.</p>
</div>

<div class="footer-elem page grey">

  <h2 class="center-text red-font ">Recherche</h2>
</br>

  <p>
    L’utilisateur compare et choisi le professionnel qui correspond le mieux à ses attentes.

  </p>

<p>
  Les recommandations d’amis, les relations en commun avec un professionnel , le système de notation par étoiles, les commentaires, le taux de réponses et le délai de réponse moyen aideront les utilisateurs à faire le bon choix.

</p>

<p>
  Le nom, prénom, entreprise et lieu de travail du professionnel sont inscrits sur son profil ainsi que le nombre de recommandations qu’il recueille. Vos relations en commun avec ce professionnel et un aperçu de qui vous les recommande sont également précisés.

</p>

<p>
  Il est important de connecter votre compte ProLinked à votre compte Facebook ou LinkedIn pour pouvoir utiliser au mieux les fonctionnalités du site. (recommandations d’amis, relations en commun avec un professionnel...)

</p>
</div>

<div class="footer-elem page grey">
  <h2 class="center-text red-font ">Profil</h2>
