<div class="footer-elem page grey">
  <h2 class="center-text red-font ">La mise en relation</h2>
</br>

<p>
  La mise en relation se fait directement à travers la plateforme ProLinked.
</p>

<p>
  Une fois le bon professionnel choisit, il ne vous reste qu’à lui envoyer une demande pour commencer à échanger et déterminer un rendez-vous.
</p>

<p>
  Une alerte sera envoyée au professionnel par système de notification sur la plateforme ProLinked mais également par SMS et Email afin de maximiser les chances de réponse du professionnel.
</p>

<p>
  Les coordonnées du professionnel ne sont pas communiquées de manière à maintenir l’échange et la communication entre clients et professionnels sur la plateforme ProLinked. Ainsi, les coordonnées du professionnel restent privées tant que le professionnel n’a pas de demandes de rendez-vous confirmées.
</p>

</div>

<div class="footer-elem page grey">
  <h2 class="center-text red-font ">Les rendez-vous</h2>
</br>
<p>
  La gestion des rendez-vous vous permet d’avoir un rappel de tous vos rendez-vous programmés :
</p>

<ul>

  <li>Nom du professionnel</li>
  <li>Lieu : redirection vers les systèmes de GPS (Google Maps)</li>
  <li>Date : redirection vers les systèmes d’agenda de l’utilisateur</li>
  <li>Horaire</li>
<li>Motif du rendez-vous</li>
  <li>Système d’alerte par SMS, Email et notifications a l’approche du rendez-vous.</li>
</ul>

</div>

<div class="footer-elem page grey">
  <h2 class="center-text red-font ">La recommandation</h2>
</br>

<p>
  Il est important d’aider les professionnels qui le méritent en les recommandant. Vous connaissez un bon professionnel et vous aimeriez lui rendre service en le faisant rejoindre la communauté ProLinked ? Rendez-vous dans la rubrique « Recommander » et indiquez les coordonnées de ce professionnel (Nom, email et/ou N°Tel)
</p>

<p>
  Plus les utilisateurs se recommanderont mutuellement des professionnels de qualité et mieux cela sera pour toute la communauté. L’entraide et le partage sont les valeurs phares de ProLinked. Ainsi, si un professionnel vous a satisfait, rendez-vous sur son profil, cliquez sur la mention « recommander » et faites profiter de vos bonnes adresses à vos amis sur ProLinked et ainsi qu’à la communauté.
</p>

<p>
  Vous gagnerez <span class="red-font">5€</span> par professionnel recommandé lors de son inscription ! (*dès lors qu’il aura procédé à son 1ER paiement). Vous pouvez recommander autant de professionnels que vous le souhaitez.
</p>

<p>
  Vous devez avoir un compte ProLinked pour envoyer ou déposer des recommandations.


</p>

