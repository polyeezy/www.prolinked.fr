  <h4 class="red-font">A Propos</h4>

  <p>
    Fondée en 2016, ProLinked est un réseau communautaire de confiance, de partage et d’entraide qui permet de mettre en relation des clients avec des professionnels uniquement par la recommandation, ainsi que de programmer des rendez-vous directement sur la plateforme.
  </p>

  <p>
    ProLinked révolutionne la façon de rechercher et d’être mis en relation avec un professionnel. Il est commun d'entendre les gens se demander sans cesse « Est ce que tu ne connais pas un bon électricien qui peut me régler un problème d’éclairage ? » « Je te donne les coordonnés de mon professeur de tennis, tu vas voir il est génial » « Tu connais pas un bon dentiste, j’ai un mal de dents terrible ? » « Je cherche un professeur de mathématiques qui pourrai me donner des cours de soutien ».
  </p>

  <p>
    Quand nous recherchons un professionnel, cela ne se fait jamais par hasard. Il nous l’est souvent recommandé par une personne de notre entourage (bouche à oreille). Prendre un rendez-vous avec un professionnel qui nous a été recommandé par un de nos proches à plus de valeur que de consulter un inconnu rencontré sur Internet. ProLinked permet donc de rechercher des professionnels parmi plus de 300 professions référencés et de voir suivant tel ou tel professionnel qui des mes relations recommande ce professionnel mais également quelles relations ou amis je partage en commun avec ce même professionnel.
  </p>

<p>
  ProLinked rend la recherche de professionnels plus facile, plus efficace, plus confiante, plus unique solidaire.


